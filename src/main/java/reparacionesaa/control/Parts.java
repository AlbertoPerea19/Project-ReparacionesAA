package reparacionesaa.control;

public class Parts {
    
    private String headphonesJack;
    private String bumperBoton;
    private String joystickReplacement;
    private String bumperMicroswitch;
    private String joystickCaps;
    private String conductorGums;


    public Parts() {
    }

    public String getHeadphonesJack() {
        return this.headphonesJack;
    }

    public void setHeadphonesJack(String headphonesJack) {
        this.headphonesJack = headphonesJack;
    }

    public String getBumperBoton() {
        return this.bumperBoton;
    }

    public void setBumperBoton(String bumperBoton) {
        this.bumperBoton = bumperBoton;
    }

    public String getJoystickReplacement() {
        return this.joystickReplacement;
    }

    public void setJoystickReplacement(String joystickReplacement) {
        this.joystickReplacement = joystickReplacement;
    }

    public String getBumperMicroswitch() {
        return this.bumperMicroswitch;
    }

    public void setBumperMicroswitch(String bumperMicroswitch) {
        this.bumperMicroswitch = bumperMicroswitch;
    }

    public String getJoystickCaps() {
        return this.joystickCaps;
    }

    public void setJoystickCaps(String joystickCaps) {
        this.joystickCaps = joystickCaps;
    }

    public String getConductorGums() {
        return this.conductorGums;
    }

    public void setConductorGums(String conductorGums) {
        this.conductorGums = conductorGums;
    }





}
